using System.Collections;
using System.Collections.Generic;
using UnityEngine;

public class GridPosition
{
    public float x;
    public float y;

    public GridPosition()
    {
        x = 0;
        y = 0;
    }

    public GridPosition(float x, float y)
    {
        this.x = x;
        this.y = y;
    }
}
